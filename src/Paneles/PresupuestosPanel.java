    public void cargarDatos() {
        PresupuestosDAO presupuestosDAO = new PresupuestosDAO();
        List<Presupuestos> listaPresupuestos = presupuestosDAO.obtenerPresupuestos();
        EmpleadosDAO empleadosDAO = new EmpleadosDAO();
        ClientesDAO clientesDAO = new ClientesDAO();
        
        
        DefaultTableModel model = new DefaultTableModel(new String[]{
            "Id", "Nombre", "Estado", "Cliente", "Empleado", "Fecha"
        }, 0);
        
        for (Presupuestos p : listaPresupuestos) {
           Empleados empleado = empleadosDAO.obtenerEmpleadoPorId(p.getEmpleados_idEmpleados());
           Clientes cliente = clientesDAO.obtenerClientePorId(p.getClientes_idClientes());
           
           if (!mostrarObjetosEliminados) {
               if (p.getEstatusPresupuestos() == 1) {
                    model.addRow(new Object[]{
                        p.getIdPresupuesto(), p.getNombrePres(), p.getEstatus(),
                        cliente.getNombreClie() + " " + cliente.getApellidosClie(), 
                        empleado.getNombreEmp() + " " + empleado.getApellidoEmp(), 
                        p.getFechaMovimiento()
                    });
               }
           } else {
               if (p.getEstatusPresupuestos() == 0) {
                    model.addRow(new Object[]{
                        p.getIdPresupuesto(), p.getNombrePres(), p.getEstatus(),
                        cliente.getNombreClie() + " " + cliente.getApellidosClie(), 
                        empleado.getNombreEmp() + " " + empleado.getApellidoEmp(), 
                        p.getFechaMovimiento()
                    });
               }
           }
        }
        
        tablaPresupuestos.setModel(model);
        
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tablaPresupuestos.setRowSorter(sorter);
        
        txtfBuscarPresupuesto.getDocument().addDocumentListener(new DocumentListener() {
            
        public void insertUpdate(DocumentEvent e) {
            filter();
        }

        public void removeUpdate(DocumentEvent e) {
            filter();
        }

        public void changedUpdate(DocumentEvent e) {
            filter();
        }

        private void filter() {
            String text = txtfBuscarPresupuesto.getText();
            if (text.trim().length() == 0) {
                sorter.setRowFilter(null);
            } else {
                sorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
            }
        }
        });
    }
    

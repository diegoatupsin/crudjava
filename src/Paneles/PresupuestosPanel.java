    private void btnVerDetallesPresupuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerDetallesPresupuestoActionPerformed
        int selectedRow = tablaPresupuestos.getSelectedRow();
        if (selectedRow != -1) {
            // Get the ID from the table itself
            int idPresupuesto = (int) tablaPresupuestos.getValueAt(selectedRow, 0); // assuming ID is in column 0

            PresupuestosDAO presupuestosDAO = new PresupuestosDAO();
            List<Presupuestos> listaPresupuestos = presupuestosDAO.obtenerPresupuestos();
            // Find the matching Presupuesto in the list
            Presupuestos selectedPresupuesto = null;
            for (Presupuestos p : listaPresupuestos) {
                if (p.getIdPresupuesto() == idPresupuesto) {
                    selectedPresupuesto = p;
                    break;
                }
            }

            if (selectedPresupuesto != null) {
                DetallesPresupuestoPanel nuevoDetallesPresupuesto = new DetallesPresupuestoPanel(selectedPresupuesto, this, usuario);                
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el presupuesto con ID " + idPresupuesto);
            }

        } else {
                JOptionPane.showMessageDialog(null, "Selecciona un presupuesto primero.");
        }
    }//GEN-LAST:event_btnVerDetallesPresupuestoActionPerformed


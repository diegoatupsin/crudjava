    private void btnReactivarPresupuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReactivarPresupuestoActionPerformed
        if(!mostrarObjetosEliminados) {
            return;
        }

        int row = tablaPresupuestos.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un item a reactivar");
            return;
        }

        int presupuestoId = (int) tablaPresupuestos.getValueAt(row, 0);
        String nombrePresupuesto = (String) tablaPresupuestos.getValueAt(row, 1);

        int confirm = JOptionPane.showConfirmDialog(this,
            "Estas seguro que quieres reactivar este presupuesto: " + nombrePresupuesto + "?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        PresupuestosDAO presupuestosDAO = new PresupuestosDAO();

        try {
            boolean eliminado;
            eliminado = presupuestosDAO.reactivarPresupuesto(presupuestoId);

            if (eliminado) {
                JOptionPane.showMessageDialog(this, "Presupuesto reactivado");
                cargarDatos(); // actualiza la tabla
            } else {
                JOptionPane.showMessageDialog(this, "Presupuesto no encontrado");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error reactivando presupuesto");
        }
    }//GEN-LAST:event_btnReactivarPresupuestoActionPerformed

